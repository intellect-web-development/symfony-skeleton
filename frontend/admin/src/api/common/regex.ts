/**
 * e3069f6b-f8da-4f61-ad95-8d4b7b249e34
 */
export function uuid() {
  return new RegExp(/^[0-9A-F]{8}-[0-9A-F]{4}-4[0-9A-F]{3}-[89AB][0-9A-F]{3}-[0-9A-F]{12}$/i);
}

/**
 * 2020-02-22
 */
export function date() {
  return new RegExp(/([0-9]{4}[-](0[1-9]|1[0-2])[-]([0-2]{1}[0-9]{1}|3[0-1]{1})|([0-2]{1}[0-9]{1}|3[0-1]{1})[-](0[1-9]|1[0-2])[-][0-9]{4})$/);
}

/**
 * 2020-02-22 12:00:00
 */
export function datetime() {
  return new RegExp(/([0-9]{4}[-](0[1-9]|1[0-2])[-]([0-2]{1}[0-9]{1}|3[0-1]{1})|([0-2]{1}[0-9]{1}|3[0-1]{1})[-](0[1-9]|1[0-2])[-][0-9]{4}) (\d{2}):(\d{2}):(\d{2})$/);
}

/**
 * 2020-02-22 or 2020-02-22 12:00:00
 */
export function dateOrDatetime() {
  return new RegExp(/([0-9]{4}[-](0[1-9]|1[0-2])[-]([0-2]{1}[0-9]{1}|3[0-1]{1})|([0-2]{1}[0-9]{1}|3[0-1]{1})[-](0[1-9]|1[0-2])[-][0-9]{4})( (\d{2}):(\d{2}):(\d{2}))?$/);
}

/**
 * 1 / 5 / -99 / etc...
 */
export function number() {
  return new RegExp(/^-?\d+\.?\d*$/);
}
